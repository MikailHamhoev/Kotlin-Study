fun main() {
    // Float
    val oneFloat : Float = 1.0f
    val twoFloat = 2f
    val threeFloat = 3F
    val fourFloat = 4.0F

    // Double
    val fiveDouble : Double = 5.0
    val sixDouble = 6.0

    println("This is a float four: $fourFloat, and here is a double six: $sixDouble.")
    println()

    val maxFloatValue = Float.MAX_VALUE
    val minFloatValue = Float.MIN_VALUE
    println("Maximum float value is $maxFloatValue")
    println("Minimum float value is $minFloatValue")
    println()

    val maxDoubleValue = Double.MAX_VALUE
    val minDoubleValue = Double.MIN_VALUE
    println("Maximum double value is $maxDoubleValue")
    println("Minimum double value is $minDoubleValue")
}
